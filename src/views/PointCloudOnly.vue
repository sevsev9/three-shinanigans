<script lang="ts">
import { defineComponent, ref } from 'vue'
import * as THREE from "three";

export default defineComponent({
  setup() {
    return {
      content: ref<HTMLDivElement>(),
      mouseX: 0,
      mouseY: 0,
      scroll_top: 0,
      renderer: undefined as THREE.WebGLRenderer | undefined,
      canvas: undefined as HTMLCanvasElement | undefined,
      camera: undefined as THREE.PerspectiveCamera | undefined,
      camera_settings: {
        fov: 75,
        aspect: 2,
        near: 0.1,
        far: 10
      },
      cube: undefined as THREE.Mesh | undefined,
      cube_settings: {
        width: 100,
        height: 100,
        depth: 100
      },
      scene: undefined as THREE.Scene | undefined,
      points: undefined as THREE.Points | undefined,
      points_settings: {
        psize: 0.05,
        pcolor: 0x44aa88,
        pamount: 15000
      }
    }
  },
  mounted() {
    const canvas = document.getElementById("background");

    window.addEventListener("mousemove", (e: MouseEvent) => {
      this.mouseX = e.clientX;
      this.mouseY = e.clientY;
    });

    this.content?.addEventListener("scroll", this.scrollHandler);

    if (canvas) {
      document.addEventListener("DOMContentLoaded", () => {
        canvas.style.opacity = "1";
      });

      // Main Scene
      this.scene = new THREE.Scene();
      const axesHelper = new THREE.AxesHelper( 5 );
      axesHelper.setColors(
        new THREE.Color("rgb(255,0,0)"),  // x = red
        new THREE.Color("rgb(0,255,0)"),  // y = green
        new THREE.Color("rgb(0,0,255)")   // z = blue
      )
      this.scene.add(axesHelper);


      this.camera_settings.aspect = canvas.clientHeight / canvas.clientWidth;

      const { fov, aspect, near, far } = this.camera_settings;
      this.camera = new THREE.PerspectiveCamera(fov, aspect, near, far);
      this.camera.position.z = 5;

      // light source
      const color = 0xffffff, intensity = 1;
      const light = new THREE.DirectionalLight(color, intensity);
      light.position.set(-1, 2, 4);
      this.scene.add(light);

      // points
      const texture = new THREE.TextureLoader().load("/sparkle_star.png");

      const { psize, pcolor, pamount } = this.points_settings;
      const points_geometry = new THREE.BufferGeometry();
      points_geometry.setAttribute("position", new THREE.Float32BufferAttribute(this.getRandomParticlePos(pamount), 3));

      const points_material = new THREE.PointsMaterial({
        size: psize,
        transparent: true,
        alphaTest: 0.5,
        map: texture
      });

      this.points = new THREE.Points(points_geometry, points_material);
      this.scene.add(this.points);

      // renderer and animation loop
      this.renderer = new THREE.WebGLRenderer({ antialias: true, canvas });
      requestAnimationFrame(this.render);
    }
  },
  methods: {
    getRandomParticlePos(particleCount: number) {
      const arr = new Float32Array(particleCount * 3);

      for (let i = 0; i < particleCount; i+=3) {
        arr[i]   = (Math.random() - 0.5) * 10; //x (this context: left, right)
        arr[i+1] = (Math.random() - 0.5) * 20; //y (this context: top, bottom)
        arr[i+2] = (Math.random() - 0.5) * 10; //z (this context: front, back)
      }      

      return arr;
    },
    resizeRendererToDisplaySize(renderer: THREE.Renderer) {
      const canvas = renderer.domElement;

      const width = canvas.clientWidth;
      const height = canvas.clientHeight;

      const needResize = canvas.width !== width || canvas.height !== height;

      // resize only when necessary
      if (needResize) {
        //3rd parameter `false` to change the internal canvas size
        renderer.setSize(width, height, false);
      }
      return needResize;
    },
    render(time: number) {
      time *= 0.001; // seconds

      if (this.renderer && this.camera && this.points) {
        const renderer = this.renderer;
        const camera = this.camera;
        const points = this.points;

        if (this.resizeRendererToDisplaySize(renderer)) {
          const canvas = this.renderer.domElement;
          camera.aspect = canvas.clientWidth / canvas.clientHeight;
          camera.updateProjectionMatrix();
        }

        points.position.y = (this.mouseY * 0.0001) + this.scroll_top;
        points.position.x = -this.mouseX * 0.0001;

        points.rotation.x = time * 0.01;
        points.rotation.y = time * 0.01;

        // render the scene
        renderer.render(this.scene!, camera);

        // loop
        requestAnimationFrame(this.render);
      }
    },
    scrollHandler() {
      this.scroll_top = this.content ? (this.content.scrollTop * 0.001) : 0;

      console.log('Scroll Top: ', (this.scroll_top).toFixed(2));
      
    }
  },
  beforeUnmount() {
      this.content?.removeEventListener('scroll', this.scrollHandler)
  },
})
</script>

